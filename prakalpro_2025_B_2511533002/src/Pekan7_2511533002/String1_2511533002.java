public class String1_2511533002 {

	public static void main(String[] args) {
	String salam = "Assalamualaikum";
	System.out.println("Panjang salam adalah : "+salam.length());
	System.out.println(salam.toUpperCase());    //output "ASSALAMUALAIKUM"
	System.out.println(salam.toLowerCase());    //output "assalamualaikum"
	System.out.println(salam.indexOf("salam")); //output 2
	}

}
